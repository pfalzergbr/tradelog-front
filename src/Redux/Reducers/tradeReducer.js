export const tradeReducer = (state, action) => {
    switch (action.type) {
        case 'GET_TRADES':
            return state;
        case 'ADD_TRADE':
            return state;
        case 'EDIT_TRADE':
            return state;
        case 'DELETE_TRADE':
            return state;
        default:
            return state;
    }
};
